public class swapExample{
    public static void main(String[] args) {
        int a=4;
        int b=6;
        int c=8;

        int temp =a;
        a=b;
        b=c;
        c= temp;
        

        System.out.println("a="+a+"\nb="+b+"\nc="+c);
        System.out.println(" ");
    }
}